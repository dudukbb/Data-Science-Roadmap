# Görev 1: List Comprehension yapısı kullanarak car_crashes verisindeki numeric değişkenlerin isimlerini büyük
# harfe çeviriniz ve başına NUM ekleyiniz. Numeric olmayan değişkenlerin de isimleri büyümeli.
# Tek bir list comprehension yapısı kullanılmalı.
# ---------------------------------------------------------------------------------------------------------------------------------------------------

import seaborn as sns                   # seaborn kütüphanesinin takma adı
df = sns.load_dataset("car_crashes")    # seaborn'un içinde hazır gelen örnek car_crashes veri setini yükler.
df.columns                              # Bunu Pandas dataframe olarak bize verir.
df.head()


df.columns = ["NUM_"+ col.upper() if df[col].dtype != "O" else col.upper() for col in df.columns]
df.columns

"""
--------------------------------------
Beklenen Çıktı:
--------------------------------------
["NUM_TOTAL",
"NUM_SPEEDING",
"NUM_ALCOHOL",
"NUM_NOT_DISTRACTED",
"NUM_NO_PREVIOUS",
"NUM_INS_PREMIUM",
"NUM_INS_LOSSES",
"ABBREV" ]
"""

# -------------------------------------------------------------------------------------------------------------------------------------------------
# Görev 2: List Comprehension yapısı kullanarak car_crashes verisinde isminde "NO" barındırmayan
# değişkenlerin isimlerinin sonuna "FLAG" yazınız.
# -------------------------------------------------------------------------------------------------------------------------------------------------
df.columns = [col+"_FLAG" if "NO" not in col else col for col in df.columns]
df.columns

"""
Beklenen Çıktı
------------------
["TOTAL_FLAG",
"SPEEDING_FLAG",
"ALCOHOL_FLAG",
"NOT_DISTRACTED",
"NO_PREVIOUS",
"INS_PREMIUM_FLAG",
"INS_LOSSES_FLAG",
"ABBREV_FLAG" ]

Tüm değişkenlerin isimleri büyük harf olmalı.
Tek bir list comprehension yapısı ile yapılmalı.
"""

# -------------------------------------------------------------------------------------------------------------------------------------------------
# Görev 3: List Comprehension yapısı kullanarak aşağıda verilen değişken isimlerinden FARKLI olan
# değişkenlerin isimlerini seçiniz ve yeni bir dataframe oluşturunuz.
# -------------------------------------------------------------------------------------------------------------------------------------------------

og_list = ["abbrev","no_previous"]
new_df = df[[col for col in df.columns if col not in og_list]]   # içerde tuttuğumuz liste onu df'in kolonları listesi şeklinde kullandık
new_df.head()

